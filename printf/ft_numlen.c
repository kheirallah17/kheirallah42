#include "ft_printf.h"

int	numlen(int n)
{
	long	nb;
	int		len;

	len = 0;
	nb = n;
	if (nb < 0)
	{
		len++;
		nb *= -1;
	}
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
